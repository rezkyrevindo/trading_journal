import 'package:dartz/dartz.dart';
import 'package:dio/dio.dart';
import 'package:trading_journal/data/api/contact/contact_api_impl.dart';
import 'package:trading_journal/data/models/contact_response.dart';
import 'package:trading_journal/models/not_found_message.dart';
import 'package:trading_journal/models/server_error_message.dart';

import '../../../models/exception_message.dart';
import 'contact_repositories.dart';

class ContactRepositoriesImpl implements ContactRepositories {
  ContactApiImpl api = ContactApiImpl();

  @override
  Future<Either<ExceptionMessage, ContactResponse?>> listContact() async {
    ContactResponse? result;
    Response? response;
    final listContactResponse = await api.listContact();
    listContactResponse.fold((l) {
      return l;
    }, (r) {
      response = r;
    });
    if (response != null) {
      if (response?.statusCode == 200) {
        final Map<String, dynamic> responseData =
            response?.data as Map<String, dynamic>;
        result = ContactResponse.fromJson(responseData);
      } else if (response?.statusCode == 400) {
        return Left(NotFoundMessage());
      } else {
        return Left(ServerErrorMessage());
      }
    } else {
      return Left(ServerErrorMessage());
    }
    return Right(result);
  }
}
